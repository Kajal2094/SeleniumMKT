package org.example;
// Program to write the declaration of all data types
public class MKT_Declaration {
    public static void main(String[] args) {
int a =10;
float b = 10.4f;
char c = 'a';
String d= "String";
double e = 12.22;
long f = (long) 12.22222;

        {
            System.out.println(a);
            System.out.println(b);
            System.out.println(c);
            System.out.println(d);
            System.out.println(e);
            System.out.println(f);
        }
    }
}
